public void showTable()
{
    if(selected == null || selected.length ==0)
    {
        String cols[] = {"Orignal File Name","New File Name"};
        String data[][] = {{"Add Files...",""}};
        jTable1 = new JTable(data, cols);
        
    }
    else
    {
        numFiles = selected.length;
        changed = new File[numFiles];
        String prefix = jTextField1.getText();
        String sufix = jTextField2.getText();
        Date obj = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("dd:MM:yyyy");
        String date = sdf.format(obj);
        sdf = new SimpleDateFormat("hh:mm:ss");
        String time = sdf.format(obj);
        String cols[] = {"Orignal File Name","New File Name"};
        String data[][] = new String[selected.length][2];
        String mid ="";
        int j,k;
        j=0;
        k=0;
        int number = 1;
        for(int i=0;i<selected.length;i++)
        {
            File temp = selected[i];

            String fname = temp.getName();
            int dotPos = fname.lastIndexOf(".");
            String name = fname.substring(0,dotPos);
            ++dotPos;
            String ext = "." + fname.substring(dotPos);
            String parent = temp.getParent();
            if(listItem.equals("Orignal File Name"))
            {
                mid = name;
            }
            else if(listItem.equals("Orignal File Name-Date"))
            {
                mid = name + "-" + date ;
            }
            else if(listItem.equals("Orignal File Name-Number"))
            {
                mid = name + "-" + number;
            }
            else if(listItem.equals("Orignal File Name-Date-Time"))
            {
                mid = name + "-" +  date + "-" + time;
            }
            else if(listItem.equals("Number"))
            {
                mid = ""+ number ;
            }
            else if(listItem.equals("Date-Number"))
            {
                mid = date + "-" + number ;
            }
            else
            {

            }
            String newName = prefix + mid + sufix + ext;
            number++;
            data[j][k] = fname;
            data[j][++k] = newName;
            k=0;
            j++;
            String fullNewPath = parent + "\\" + newName;
            changed[i] = new File(fullNewPath);
            //System.out.println(changed[i].getAbsolutePath());
        }
        jTable1 = new JTable(data,cols);
        
    }
    jScrollPane1.setViewportView(jTable1);
}

