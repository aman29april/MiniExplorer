private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

    if(selected != null && selected.length !=0)
    {
        for(int i=0;i<selected.length;i++)
        {
            boolean check = selected[i].renameTo(changed[i]);
            if(check == false)
            {
                JOptionPane.showMessageDialog(null, "cant rename " +selected[i].getName() + " file as file named "+ changed[i].getName() + " already exists");
            }
            System.out.println(check);
        }
        selected = changed;
        showTable();
        JOptionPane.showMessageDialog(null, "Conversion Completed..");        
    }
    else
    {
        JOptionPane.showMessageDialog(this, "Add some files to rename..","Error Msg",JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_jButton4ActionPerformed

